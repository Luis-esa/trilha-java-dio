public class ForArrays {
    public static void main(String[] args) {
        //Em arrays o indice inicia-se em 0
        String alunos [] = {"FELIPE", "MARIA", "JOAO","MARCOS"};
        for (String aluno : alunos){
            System.out.println("Aluno(a) presente na aula: "+aluno);
        }
    }
}
